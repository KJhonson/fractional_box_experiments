#' Mass lumping operator
#'
#' @param A Matrix. Input matrix to apply mass lumping.
#' @return Matrix. Diagonal matrix where each diagonal element is the sum of
#'   the corresponding row of A.
#' @details
#' Implements the mass lumping operator L from equation (4.1):
#' L(A)_{ij} = δ_{ij} Σ_{k=1}^{N_h} A_{ik}
#' This operator maps a matrix to a diagonal matrix where each diagonal
#' element equals the sum of the corresponding row.
mass_lumping_operator <- function(A) {
  if (!inherits(A, "Matrix")) {
    A <- as(A, "Matrix")
  }
  # Compute row sums
  row_sums <- Matrix::rowSums(A)
  # Create diagonal matrix with row sums
  L_A <- Matrix::Diagonal(x = row_sums)
  return(L_A)
}


#' Compute modified mass matrix M_i (c2) from Proposition 4.1
#'
#' @param M Matrix. Classical mass matrix (e.g., c0 from fem_matrices).
#' @param bandwidth Integer. Bandwidth parameter i (1 ≤ i ≤ N_h), where N_h is
#'   the dimension of M. Controls how much of the original matrix structure
#'   is preserved.
#' @return Matrix. Modified mass matrix M_i = D_i + L(R_i), where D_i preserves
#'   entries within the specified bandwidth and L(R_i) is the mass-lumped
#'   residual.
#' @details
#' Implements Proposition 4.1: For a given bandwidth i, decomposes the mass
#' matrix M as M = D_i + R_i, where D_i contains entries within bandwidth i
#' and R_i is the residual. Returns M_i = D_i + L(R_i), where L is the mass
#' lumping operator.
#'
#' The bandwidth of length i means entries are kept where |row_index -
#' col_index| ≤ i-1. For i = 1, only the diagonal is kept. For larger i, a
#' band around the diagonal is preserved.
#'
#' @examples
#' # Create a mesh and get the mass matrix
#' env <- base_env(4, "LM", 2, kappa = 1)
#' M <- env$fem_matrices$c0
#' # Compute M_i for different bandwidths
#' M_1 <- compute_c2(M, bandwidth = 1)  # Only diagonal + lumped rest
#' M_5 <- compute_c2(M, bandwidth = 5)   # Wider band preserved
compute_m <- function(M, bandwidth = 1) {
  if (!inherits(M, "Matrix")) {
    M <- as(M, "Matrix")
  }
  N_h <- nrow(M)
  if (ncol(M) != N_h) {
    stop("Matrix M must be square.")
  }
  if (bandwidth < 1 || bandwidth > N_h) {
    stop("bandwidth must satisfy 1 <= bandwidth <= N_h")
  }
  if (inherits(M, "sparseMatrix")) {
    M_triplet <- as(M, "TsparseMatrix")

    row_idx <- M_triplet@i + 1L
    col_idx <- M_triplet@j + 1L
    values <- M_triplet@x

    band_mask <- abs(row_idx - col_idx) <= (bandwidth - 1)

    D_i <- sparseMatrix(
      i = row_idx[band_mask],
      j = col_idx[band_mask],
      x = values[band_mask],
      dims = c(N_h, N_h),
      dimnames = dimnames(M)
    )
  } else {
    row_mat <- matrix(rep(1:N_h, each = N_h), N_h, N_h)
    col_mat <- matrix(rep(1:N_h, N_h), N_h, N_h)
    band_mask <- abs(row_mat - col_mat) <= (bandwidth - 1)

    D_i <- M
    D_i[!band_mask] <- 0
  }
  R_i <- M - D_i
  L_R_i <- mass_lumping_operator(R_i)
  M_i <- D_i + L_R_i
  return(M_i)
}


#' Create a base environment for finite element mesh and matrices
#'
#' @param ref_level Numeric or numeric vector. Controls the mesh resolution. The number of grid points is set to 2^ref_level + 1, so increasing 'ref_level' increases the number of points and thus the mesh resolution. If a vector is provided, returns a list of base_env results.
#' @param type Character. Type of matrix to use for L ("LM" for g1 + c0, "L2" for g1 + c1).
#' @param dimension Integer. Spatial dimension (1 or 2).
#' @return If ref_level is a single value: A named list with four elements. If ref_level is a vector: A list of named lists, one for each ref_level. Each list contains: \describe{
#'   \item{mesh}{The generated mesh object (either 1D or 2D, depending on \code{dimension}).}
#'   \item{fem_matrices}{A list containing the finite element matrices: \code{c0} (lumped mass matrix), \code{c1} (mass matrix), \code{g1} (stiffness of the laplacian), and \code{m2} (modified mass matrix from Proposition 4.1 with bandwidth=2).}
#'   \item{operator_matrix}{The matrix constructed according to the selected \code{type} argument.}
#'   \item{h}{The mesh size, computed as 1/2^{ref_level}.}
#' }
#' @details
#' Mesh generation according to mesh ref_level:
#' For both 1D and 2D, the mesh is generated by dividing the interval [0,1] (or [0,1]x[0,1] for 2D) into (2^ref_level) equal segments, resulting in (2^ref_level + 1) equally spaced points along each axis. For example, if ref_level = 4, there will be 17 points (2^4 + 1) along each axis.
#' For dimension = 1, creates a 1D mesh on [0,1] with Neumann boundary conditions.
#' For dimension = 2, creates a 2D mesh on [0,1]x[0,1] using a regular lattice and constrained Delaunay triangulation.
#' The matrix L is constructed according to the selected type.
base_env <- function(ref_level, type, dimension, kappa){
  
  # If ref_level is missing, throw an error
  if (missing(ref_level)) {
    stop("'ref_level' must be specified.")
  }
  if (missing(type)) {
    stop("'type' must be specified.")
  }
  if (missing(dimension)) {
    stop("'dimension' must be specified.")
  }
  if (missing(kappa)) {
    stop("'kappa' must be specified.")
  }
  
  # If ref_level is a vector, create multiple base_env structures
  if (length(ref_level) > 1) {
    result_list <- list()
    for (i in seq_along(ref_level)) {
      result_list[[i]] <- base_env(ref_level[i], type, dimension, kappa)
      names(result_list)[i] <- paste0("ref_level_", ref_level[i])
    }
    return(result_list)
  }
  
  N.ok <- ref_level  # Mesh resolution parameter

  # The number of points in each dimension is (2^N.ok + 1)
  n_points <- 2^N.ok + 1
  # Compute mesh size h
  h <- 1 / (2^N.ok)

  if (dimension == 1) {
    # Create a 1D lattice of points from 0 to 1, with n_points equally spaced points
    lattice <- seq(0, 1, length.out = n_points)
    # Generate a 1D mesh with Neumann boundary conditions
    mesh <- fm_mesh_1d(loc = lattice, boundary = c("neumann", "neumann"))
    # Compute finite element matrices for the mesh
    fem <- fm_fem(mesh)
    c0 <- fem$c0  # Lumped mass matrix
    c1 <- fem$c1  # Mass Matrix
    g1 <- fem$g1  # Stiffness of the laplacian

    # Compute m2 using Proposition 4.1 with bandwidth=2
    m2 <- compute_m(c1, bandwidth = 2)

    # Select the matrix L based on the type argument
    if (type == "LM") {
      L <- g1 + kappa*c0
    } else if (type == "L2") {
      L <- g1 + kappa*c1
    } else {
      stop("Unknown type. Use 'LM' or 'L2'.")
    }

    # Collect FEM matrices in a list
    fem_matrices <- list(c0 = c0, c1 = c1, g1 = g1, m2 = m2)
    # Return a named list: mesh, fem_matrices, operator_matrix, and h
    result <- list(mesh = mesh, fem_matrices = fem_matrices, operator_matrix = L, h = h)

  } else if (dimension == 2) {
    # Create a 2D lattice of points on [0,1] x [0,1], each axis with n_points equally spaced points
    lattice <- fm_lattice_2d(
      seq(0, 1, length.out = n_points),
      seq(0, 1, length.out = n_points)
    )
    # Generate a 2D mesh using constrained Delaunay triangulation
    mesh <- fm_rcdt_2d_inla(lattice = lattice, boundary = lattice$segm, extend = FALSE)
    # Compute finite element matrices for the mesh
    fem <- fm_fem(mesh)
    c0 <- fem$c0  # Lumped mass matrix
    c1 <- fem$c1  # Mass Matrix
    g1 <- fem$g1  # Stiffness of the laplacian

    # Compute m2 using with bandwidth=2
    m2 <- compute_m(c1, bandwidth = 2)

    # Select the matrix L based on the type argument
    if (type == "LM") {
      L <- g1 + kappa*c0
    } else if (type == "L2") {
      L <- g1 + kappa*c1
    } else {
      stop("Unknown type. Use 'LM' or 'L2'.")
    }

    # Collect FEM matrices in a list
    fem_matrices <- list(c0 = c0, c1 = c1, g1 = g1, m2 = m2)
    # Return a named list: mesh, fem_matrices, operator_matrix, and h
    result <- list(mesh = mesh, fem_matrices = fem_matrices, operator_matrix = L, h = h)

  } else {
    # Only 1D and 2D are supported
    stop("Unsupported dimension. Use 1 or 2.")
  }

  return(result)
} 

