#' Compute the load term for the fractional SPDE
#'
#' @param mesh An fmesh object representing the finite element mesh.
#' @param L Matrix. Operator matrix (e.g., stiffness or mass matrix), should be compatible with Matrix package methods.
#' @param C Matrix. Covariance or mass matrix, should be compatible with Matrix package methods and diagonal.
#' @param f Numeric vector. Right-hand side/load vector.
#' @param beta Numeric. Fractional exponent parameter (default: 1).
#' @param scale.factor Numeric. Scaling factor for the operator (default: 1).
#' @param m Integer. Order of the operator (default: 1).
#' @param tau Numeric. Precision parameter (default: 1).
#'
#' @return Numeric vector. Solution to the fractional SPDE load term.
#' 
#' @details
#' This function computes the load term for a fractional SPDE using the provided mesh and operator matrices.
#' The mesh must be an fmesh object. L and C must be matrices compatible with the Matrix package (e.g., Matrix, sparseMatrix, or base matrix).
#'
rSPDE_solver <- function(mesh, L, C, f, beta = 1, scale.factor = 1, m = 1, tau = 1) {
  # Compute the fractional operator matrices
  rSpde_fem <- fractional.operators(L = L, C = C, beta = beta, scale.factor = scale.factor, m = m, tau = tau)
  P_r <- rSpde_fem$Pr  # Right operator matrix
  P_l <- rSpde_fem$Pl  # Left operator matrix
  # Solve the system: P_l * x = f, then apply P_r
  load <- P_r %*% Matrix::solve(P_l, f)
  
  return(load)
}

